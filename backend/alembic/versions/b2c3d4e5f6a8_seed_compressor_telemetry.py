    """
    Categorize asset_id=1 as 'compressor' and seed it with real Azure
    Predictive Maintenance telemetry (machineID=99, the week leading
    up to a real documented failure).

    Idempotent: re-running this migration on a database already in
    this state makes no further changes — it checks for the specific
    asset_type and telemetry rows before inserting, rather than
    blindly inserting duplicates.
    """
    connection = op.get_bind()

    # Step 1 — re-categorize asset_id=1 to compressor, if it exists
    # and isn't already categorized that way
    compressor_type = connection.execute(
        sa.text("SELECT id FROM asset_types WHERE name = 'compressor'")
    ).first()
    if compressor_type is not None:
        asset = connection.execute(
            sa.text("SELECT id, asset_type_id FROM assets WHERE id = 1")
        ).first()
        if asset is not None and asset[1] != compressor_type[0]:
            connection.execute(
                sa.text("UPDATE assets SET asset_type_id = :type_id WHERE id = 1"),
                {"type_id": compressor_type[0]},
            )

    # Step 2 — only seed telemetry if asset_id=1 exists and has none
    # of THIS migration's specific timestamps yet (idempotency check)
    asset_exists = connection.execute(
        sa.text("SELECT id FROM assets WHERE id = 1")
    ).first()
    if asset_exists is None:
        return

    first_ts = _TELEMETRY_DATA[0][0]
    already_seeded = connection.execute(
        sa.text(
            "SELECT id FROM telemetry_records "
            "WHERE asset_id = 1 AND timestamp = :ts"
        ),
        {"ts": first_ts},
    ).first()
    if already_seeded is not None:
        return

    for ts, voltage, rotation, pressure, vibration in _TELEMETRY_DATA:
        payload = {
            "voltage": voltage,
            "rotation": rotation,
            "pressure": pressure,
            "vibration": vibration,
        }
        connection.execute(
            sa.text(
                "INSERT INTO telemetry_records (asset_id, timestamp, payload) "
                "VALUES (1, :ts, :payload)"
            ),
            {"ts": ts, "payload": json.dumps(payload)},
        )



