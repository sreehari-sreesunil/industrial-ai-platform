"""
MLPrediction ORM model.
Defines the inference history entity and its relationships.
"""
from datetime import datetime
from sqlalchemy import (
    DateTime,
    Float,
    ForeignKey,
    String,
    Text,
)
from sqlalchemy.orm import (
    Mapped,
    mapped_column,
    relationship,
)
from app.db.base_class import Base


class MLPrediction(Base):
    """
    Inference history entity.
    Records every prediction made by a model against an asset.
    Append-only — never updated after insert.
    Required for drift detection and post-incident forensic analysis.
    """

    __tablename__ = "ml_predictions"

    # Primary key
    id: Mapped[int] = mapped_column(
        primary_key=True,
    )

    # NULL when prediction was generated by rule-based logic, not a model
    model_id: Mapped[int | None] = mapped_column(
        ForeignKey("ml_models.id"),
        nullable=True,
    )

    # Asset this prediction was made against
    asset_id: Mapped[int] = mapped_column(
        ForeignKey("assets.id"),
        nullable=False,
    )

    # When this prediction was generated
    timestamp: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        nullable=False,
    )

    # What kind of prediction this row represents
    # Values: 'failure_probability' | 'anomaly_score' | 'health_score'
    prediction_type: Mapped[str] = mapped_column(
        String(100),
        nullable=False,
    )

    # Primary output value
    # Range: 0-1 for probabilities, 0-100 for health scores
    score: Mapped[float] = mapped_column(
        Float,
        nullable=False,
    )

    # How confident the model is in this prediction
    # NULL when algorithm does not produce calibrated confidence scores
    confidence: Mapped[float | None] = mapped_column(
        Float,
        nullable=True,
    )

    # Human-readable risk classification derived from score
    # Values: 'low' | 'medium' | 'high' | 'critical'
    risk_level: Mapped[str | None] = mapped_column(
        String(50),
        nullable=True,
    )

    # JSON snapshot of the feature vector used for this prediction
    # Deserialized via json.loads() in the service layer — never queried by DB
    feature_values: Mapped[str | None] = mapped_column(
        Text,
        nullable=True,
    )

    # JSON dict of feature importances at inference time
    # Example: '{"temperature_zscore": 0.34, "vibration_rolling_std": 0.28}'
    # Deserialized via json.loads() in the service layer — never queried by DB
    explanation: Mapped[str | None] = mapped_column(
        Text,
        nullable=True,
    )

    # Set by database at insert time — never set by application code
    created_at: Mapped[datetime] = mapped_column(
        DateTime(timezone=True),
        server_default="NOW()",
    )

    # --- Relationships ---------------------------------------------------

    # Model that generated this prediction (bidirectional)
    model = relationship(
        "MLModel",
        back_populates="predictions",
    )

    # Asset this prediction was made against (unidirectional — Option B)
    asset = relationship(
        "Asset",
    )